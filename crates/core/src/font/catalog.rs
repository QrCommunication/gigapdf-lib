//! A curated font catalog — the families the engine knows how to resolve.
//!
//! "Include the maximum of fonts" without bloating the WASM binary by megabytes
//! means shipping **metadata**, not the TTF bytes: the catalog lists ~1950
//! families (the PDF/system standard set + the full Google Fonts library) with
//! their category, common weights, and whether they are fetchable from Google
//! Fonts (so the host can download them on demand and cache). System / PDF
//! standard families are `google = false` (the host substitutes a local or
//! metric-compatible face). Generated from the public Google Fonts metadata;
//! re-run `tools/gen_catalog.py` to refresh.

/// Generic classification of a family.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Category {
    Sans,
    Serif,
    Mono,
    Display,
    Handwriting,
}

impl Category {
    pub fn as_str(self) -> &'static str {
        match self {
            Category::Sans => "sans-serif",
            Category::Serif => "serif",
            Category::Mono => "monospace",
            Category::Display => "display",
            Category::Handwriting => "handwriting",
        }
    }
}

/// A catalog entry.
#[derive(Debug, Clone, Copy)]
pub struct FontInfo {
    pub family: &'static str,
    pub category: Category,
    /// Available on Google Fonts (host-fetchable). `false` = system/standard.
    pub google: bool,
    /// Common weights offered.
    pub weights: &'static [u16],
}

