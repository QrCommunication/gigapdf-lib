/// Case-insensitive family aliases -> canonical catalog name.
fn canonical(family: &str) -> &str {
    let lower = family.trim().to_ascii_lowercase();
    match lower.as_str() {
        "times" | "times roman" | "timesnewroman" | "times-roman" => "Times New Roman",
        "courier" | "couriernew" => "Courier New",
        "arialmt" | "helvetica neue" | "helveticaneue" => "Arial",
        "sans-serif" | "sans" => "Helvetica",
        "serif" => "Times New Roman",
        "monospace" | "mono" => "Courier New",
        _ => "",
    }
}

/// Look up a family by name (case-insensitive, alias-aware).
pub fn lookup(family: &str) -> Option<&'static FontInfo> {
    let target = family.trim();
    if let Some(found) = CATALOG
        .iter()
        .find(|f| f.family.eq_ignore_ascii_case(target))
    {
        return Some(found);
    }
    let canon = canonical(target);
    if canon.is_empty() {
        return None;
    }
    CATALOG.iter().find(|f| f.family == canon)
}

/// Is `family` fetchable from Google Fonts (directly or via alias)?
pub fn is_google(family: &str) -> bool {
    lookup(family).is_some_and(|f| f.google)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn catalog_is_substantial_and_unique() {
        assert!(
            CATALOG.len() >= 1000,
            "broad catalog ({} families)",
            CATALOG.len()
        );
        // No duplicate family names.
        let mut names: Vec<&str> = CATALOG.iter().map(|f| f.family).collect();
        names.sort_unstable();
        let before = names.len();
        names.dedup();
        assert_eq!(before, names.len(), "duplicate family names present");
    }

    #[test]
    fn category_as_str_all_variants() {
        assert_eq!(Category::Sans.as_str(), "sans-serif");
        assert_eq!(Category::Serif.as_str(), "serif");
        assert_eq!(Category::Mono.as_str(), "monospace");
        assert_eq!(Category::Display.as_str(), "display");
        assert_eq!(Category::Handwriting.as_str(), "handwriting");
    }

    #[test]
    fn lookup_is_case_insensitive_and_aliased() {
        assert_eq!(lookup("roboto").unwrap().family, "Roboto");
        assert_eq!(lookup("ROBOTO").unwrap().family, "Roboto");
        assert_eq!(lookup("Times").unwrap().family, "Times New Roman");
        assert_eq!(lookup("ArialMT").unwrap().family, "Arial");
        assert!(lookup("NoSuchFont").is_none());
    }

    #[test]
    fn google_availability() {
        assert!(is_google("Open Sans"));
        assert!(is_google("inter"));
        assert!(!is_google("Helvetica"));
        assert!(!is_google("Times"));
    }
}
